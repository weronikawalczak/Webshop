package com.codecool.shop.model;

public class Address {
    String country;
    String city;
    String zipcode;
    String adress;

    public Address(String country, String city, String zipcode, String adress) {
        this.country = country;
        this.city = city;
        this.zipcode = zipcode;
        this.adress = adress;
    }
}
